valor1 =float(input("Digite um primeiro valor real: "))
valor2 =float(input("Digite um segundo valor real: "))

adicao = valor1 + valor2
subtracao = valor1 - valor2
divisao = valor1 / valor2
multiplicacao = valor1 * valor2

print(f"Números digitados: {valor1:.1f} e {valor2:.1f}")
print(f"A soma desses números é: {adicao:.1f}")
print(f"A subtração desses números é: {subtracao:.1f}")
print(f"A multiplicação desses números é: {multiplicacao:.1f}")
print(f"A divisão desses números é: {divisao:.2f}")